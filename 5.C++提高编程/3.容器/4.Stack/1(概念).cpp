/*          5. stack 容器 (栈)

####        1. stack 基本概念

**概念: stack是一种"先进后出"(First In Last Out,FILO)的数据结构: 它只有一个出口

                    栈底         元素1
                                 元素2
                                 元素3
                    栈顶         元素4      top

                        push()>            pop()>


栈中只有顶端的元素才可以被外界使用，因此栈 "不允许有遍历行为"

栈中进入数据称为  --- **入栈**  `push

栈中弹出数据称为  --- **出栈**  `pop

    1. 栈不允许遍历行为
    2. 栈可以判断容器是否为空吗?  可以: empty()
    3. 栈可以返回元素个数吗?    可以: size()
*/
